	private byte[] toByteArray(int value, int length) {
		byte[] b = new byte[length];
		int i = 0;
		int y = 0;
		while (length - i > 0) {
			b[length - ++i] = (byte) (value >> y);
			y += 8;
		}
		return b;
	}

	private String constructMessage(String prefix, DptTestCase testCase) {
		StringBuilder b = new StringBuilder();
		b.append(prefix);
		b.append(" for [");
		b.append(testCase.getDatapoint().getDPT());
		b.append("] with data {");
		int i = 0;
		while (true) {
			b.append(testCase.getData()[i++]);
			if (i == testCase.getData().length) {
				break;
			} else {
				b.append(",");
			}
		}
		b.append("}");
		return b.toString();
	}


